# ejemplo de tipos de datos en python

# Entero
edad = 30
print('edad:',edad)

# Numbero flotante
altura = 1.75
print('Altura:',altura)

# cadena de texto
nombre = 'Ana'
print('Nombre:', nombre)

# tipo boolean
es_estudiante = False
print('Es estudiante?', es_estudiante)

# None, ausencia de valor
direccion = None
print('direccion:', direccion)